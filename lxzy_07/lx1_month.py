# 1. 将如下数据形成一个字典 
#     - 1 = "春季有1,2,3月"
#     - 2 = "夏季有4,5,6月"
#     - 3 = "秋季有7,8,9月"
#     - 4 = "东季有10,11,12月"
l_book =[
    (1,"春季有1,2,3月"),
    (2,"夏季有4,5,6月"),
    (3,"秋季有7,8,9月"),
    (4,"东季有10,11,12月")
        ] 
book =  dict(l_book)
print(book)
# 2. 让用户输入一个整数代表这个季度,打印这个挤兑对应的信息,如果用户输入的信息不存在于字典中,则提示用"信息不存在
season = int(input("请输入(1~4)的整数: "))
if season not in book:
    print("信息不存在")
else:    
    print(book[season])  